	addc_id INT PRIMARY KEY IDENTITY(1,1),
	addc_name NVARCHAR(250) NOT NULL,
	addc_descrip NVARCHAR(MAX) NOT NULL,
	addc_debit DECIMAL(10, 2) NOT NULL,
	addc_credit DECIMAL(10, 2) NOT NULL,
