CREATE TABLE BANK_PASSIVE_CORRES_ACCOUTS
(
	ca_bank_id INT PRIMARY KEY IDENTITY(1,1),
	ca_bank_name NVARCHAR(250) NOT NULL,
	ca_bank_company INT FOREIGN KEY REFERENCES BANK_CLIENT(client_id) NOT NULL,
	ca_bank_cash DECIMAL(10, 2) NOT NULL,
	ca_bank_type INT FOREIGN KEY REFERENCES BANK_CURRENCY(currency_id) NOT NULL,
);
